// Generate random positions for complete coverage
const generatePositions = () => {
  const positions = [];
  const count = 30; // Total number of icons to display
  
  for (let i = 0; i < count; i++) {
    positions.push({
      left: Math.random() * 100,
      top: Math.random() * 100,
      depth: 1 + Math.random() * 2, // 1-3 depth layers
      duration: 4 + Math.random() * 6, // 4-10 seconds animation
      delay: Math.random() * 2, // 0-2 seconds delay
    });
  }
  
  return positions;
};

function FloatingTech() {
  const mouseX = useMotionValue(0);
  const mouseY = useMotionValue(0);
  const [positions] = useState(generatePositions);

  // Smooth spring animation for cursor movement
  const springConfig = { damping: 30, stiffness: 200 };
  const smoothMouseX = useSpring(mouseX, springConfig);
  const smoothMouseY = useSpring(mouseY, springConfig);

  const handleMouseMove = (e: React.MouseEvent<HTMLDivElement>) => {
    const rect = e.currentTarget.getBoundingClientRect();
    const centerX = rect.width / 2;
    const centerY = rect.height / 2;
    const x = (e.clientX - rect.left - centerX) / centerX;
    const y = (e.clientY - rect.top - centerY) / centerY;
    
    mouseX.set(x * 50); // Multiply for stronger effect
    mouseY.set(y * 50);
  };

  return (
    <div
      onMouseMove={handleMouseMove}
      className="absolute inset-0 overflow-hidden pointer-events-none"
      aria-hidden="true"
    >
      {positions.map((position, i) => {
        const tech = techIcons[i % techIcons.length];
        
        // Parallax effect based on depth
        const xParallax = useTransform(
          smoothMouseX,
          (value) => value / position.depth
        );
        const yParallax = useTransform(
          smoothMouseY,
          (value) => value / position.depth
        );
        
        return (
          <motion.div
            key={`${tech.name}-${i}`}
            className="absolute w-12 h-12 md:w-14 md:h-14 lg:w-16 lg:h-16"
            style={{
              left: `${position.left}%`,
              top: `${position.top}%`,
              x: xParallax,
              y: yParallax,
            }}
            initial={{ 
              opacity: 0, 
              scale: 0,
              rotate: 0 
            }}
            animate={{
              opacity: [0.1, 0.25, 0.1],
              scale: [0.8, 1.1, 0.8],
              y: [-10, 10, -10],
              rotate: [0, 10, -10, 0],
            }}
            transition={{
              duration: position.duration,
              delay: position.delay,
              repeat: Infinity,
              ease: "easeInOut",
            }}
            whileHover={{
              opacity: 0.7,
              scale: 1.3,
              rotate: 15,
              transition: { duration: 0.3 }
            }}
          >
            <div className="w-full h-full opacity-60">
              {tech.icon}
            </div>
          </motion.div>
        );
      })}
    </div>
  );
}

export default FloatingTech;
